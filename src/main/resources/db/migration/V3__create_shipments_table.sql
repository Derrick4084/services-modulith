CREATE TABLE services.shipments (
                                    estimated_delivery_date date NULL,
                                    shipping_cost float8 NULL,
                                    created_at timestamp(6) NULL,
                                    delivered_at timestamp(6) NULL,
                                    id int8 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE) NOT NULL,
                                    order_id int8 NULL,
                                    shipped_at timestamp(6) NULL,
                                    updated_at timestamp(6) NULL,
                                    address_line1 varchar(255) NULL,
                                    address_line2 varchar(255) NULL,
                                    carrier varchar(255) NULL,
                                    city varchar(255) NULL,
                                    country varchar(255) NULL,
                                    phone_number varchar(255) NULL,
                                    postal_code varchar(255) NULL,
                                    recipient_name varchar(255) NULL,
                                    shipment_method varchar(255) NULL,
                                    state varchar(255) NULL,
                                    status varchar(255) NULL,
                                    tracking_number varchar(255) NULL,
                                    CONSTRAINT shipments_carrier_check CHECK (((carrier)::text = ANY ((ARRAY['FEDEX'::character varying, 'DHL'::character varying, 'UPS'::character varying, 'USPS'::character varying, 'AMAZON_LOGISTICS'::character varying, 'ON_TRAC'::character varying, 'UBER_EATS'::character varying, 'DOORDASH'::character varying, 'POST_MATES'::character varying, 'XPO_LOGISTICS'::character varying, 'OLD_DOMINION'::character varying, 'ESTES'::character varying, 'OTHER'::character varying])::text[]))),
	CONSTRAINT shipments_pkey PRIMARY KEY (id),
	CONSTRAINT shipments_shipment_method_check CHECK (((shipment_method)::text = ANY ((ARRAY['STANDARD'::character varying, 'EXPRESS'::character varying, 'OVERNIGHT'::character varying])::text[]))),
	CONSTRAINT shipments_status_check CHECK (((status)::text = ANY ((ARRAY['PENDING'::character varying, 'SHIPPED'::character varying, 'IN_TRANSIT'::character varying, 'DELIVERED'::character varying, 'FAILED'::character varying, 'RETURNED'::character varying])::text[])))
);
